void SettingsMenu::draw(Adafruit_GFX* display) const {
  display->drawBitmap(32, 0, saat_bmp, 64, 64, 1);
  display->setTextColor(WHITE);
  display->setTextSize(1);
  digitalWrite(6, HIGH);
  digitalWrite(13, HIGH);
  delay(50);
  digitalWrite(13, LOW);
  digitalWrite(6, LOW);
  delay(50);
  digitalWrite(13, HIGH);
  digitalWrite(6, HIGH);
  delay(50);
  digitalWrite(13, LOW);
  digitalWrite(6, LOW);
  delay(50);
  display->setCursor(48, 25);
  display->print(F("HELLO"));


}
